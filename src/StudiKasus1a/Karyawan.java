package StudiKasus1a;

public class Karyawan {

    private String nama;
    private String alamat;
    private String noTelp;
    private String jk;
    private String Kategori;
    Karyawan(){

    }

    public Karyawan(String nama, String alamat, String noTelp, String jk, String kategori) {
        this.nama = nama;
        this.alamat = alamat;
        this.noTelp = noTelp;
        this.jk = jk;
        Kategori = kategori;
    }
}
